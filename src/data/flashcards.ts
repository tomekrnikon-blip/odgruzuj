export const getFlashcardsByCategory = (category: Category): Flashcard[] => {
  return flashcards.filter((card) => card.category === category);
};

export const getRandomFlashcard = (
  selectedCategories: Category[],
  excludeIds: number[] = []
): Flashcard | null => {
  const availableCards = flashcards.filter(
    (card) =>
      selectedCategories.includes(card.category) && !excludeIds.includes(card.id)
  );
  
  if (availableCards.length === 0) return null;
  
  const randomIndex = Math.floor(Math.random() * availableCards.length);
  return availableCards[randomIndex];
};

